import mongoose from "mongoose";

const collectionSchema = mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  posts: [
    {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Post",
    },
  ],
  creatorId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Collection = mongoose.model("Collection", collectionSchema);
export default Collection;
